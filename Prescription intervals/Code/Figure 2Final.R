library(tidyverse)
##----given info----------------------------------------------------------------

mu <- 4.447
sigma <- sqrt(0.105)

##----Mean IAD------------------------------------------------------------------

M <- exp(mu + sigma^2 / 2)

##----g(r)----------------------------------------------------------------------

g <- function(r) {
  F_r <- plnorm(r, meanlog = mu, sdlog = sigma)
  (1-F_r) / M
}

##----Curve---------------------------------------------------------------------

curve(g, from=0, to=250, n=800,
      xlab= "Time(Days)",
      ylab= "Density",
      main= "Figure 2")

##----End-----------------------------------------------------------------------